        <h1>Sobre Nosotros</h1>
        <p>Bienvenidos a <strong>Egresit</strong>, tu aliado estratégico en la transición del ámbito académico al profesional. Nuestra misión es facilitar el camino para que los estudiantes y recién egresados encuentren oportunidades laborales y prácticas profesionales que impulsen sus carreras.</p>

        <h2>Nuestra Historia</h2>
        <p>Egresit nació de la necesidad que observamos en muchos estudiantes y egresados que luchaban por conectar con el mundo laboral. Nuestro equipo, compuesto por profesionales de recursos humanos, desarrolladores y educadores, se unió con el objetivo común de crear una plataforma que haga esta transición más fluida y accesible.</p>

        <h2>Qué Hacemos</h2>
        <p>En Egresit, entendemos los desafíos a los que te enfrentas al buscar tu primer empleo o práctica profesional. Por eso, hemos diseñado una plataforma intuitiva y fácil de usar que te permite:</p>
        <ul>
            <li><strong>Buscar Empleos y Prácticas:</strong> Explora una amplia gama de oportunidades adaptadas a tu perfil académico y profesional.</li>
            <li><strong>Crear un Perfil Profesional:</strong> Presenta tu currículum de manera atractiva y profesional para captar la atención de empleadores potenciales.</li>
            <li><strong>Recibir Orientación:</strong> Accede a recursos y consejos sobre cómo preparar tu currículum, entrevistas de trabajo y desarrollo profesional.</li>
            <li><strong>Conectar con Empresas:</strong> Establece contacto directo con empresas que buscan talento joven y entusiasta como tú.</li>
        </ul>

        <h2>Nuestra Visión</h2>
        <p>Nuestra visión es ser la plataforma líder que une a estudiantes y egresados con oportunidades laborales, contribuyendo al desarrollo profesional y al éxito de la próxima generación de líderes y profesionales.</p>

        <h2>Nuestro Compromiso</h2>
        <p>En Egresit, estamos comprometidos con tu éxito. Nos esforzamos por ofrecerte las mejores herramientas y recursos para que encuentres el trabajo o la práctica que te permita crecer y alcanzar tus metas profesionales. Valoramos la retroalimentación de nuestros usuarios y continuamente mejoramos nuestra plataforma para satisfacer tus necesidades.</p>

        <h2>Únete a Nosotros</h2>
        <p>Te invitamos a formar parte de la comunidad Egresit. Regístrate hoy y comienza a explorar las oportunidades que te esperan. Si eres una empresa en busca de talento joven y brillante, también puedes unirte a nosotros y encontrar al candidato perfecto para tu equipo.</p>

        <h2>Contacto</h2>
        <p>¿Tienes preguntas o necesitas ayuda? No dudes en contactarnos. Estamos aquí para apoyarte en cada paso de tu camino profesional.</p>
